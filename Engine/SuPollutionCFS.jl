function CogenerationCredits(data::Data,areas,market)
  (; db,year) = data
  (; ECCs) = data #sets
  (; CgECBaseYear,PSoECCBaseYear,CgEC,CreditsCogeneration,PSoECC,CgCredits,ObligatedCFS) = data

  #@debug "  SuPollutionCFS.jl - Control - CogenerationCredits"

  for area in areas, ecc in ECCs
    CreditsCogeneration[ecc,area] = 
      max(((CgEC[ecc,area]-CgECBaseYear[ecc,area])-(PSoECC[ecc,area]-PSoECCBaseYear[ecc,area])),0)*
      CgCredits[market]*ObligatedCFS[ecc,area]
  end

  WriteDisk(db,"SOutput/CreditsCogeneration",year,CreditsCogeneration)

end

function SequesteringCredits(data::Data,areas,market)
  (; db,year) = data
  (; ECC,ECCs,FuelEP,Nation,Polls,Processes) = data #sets
  (; CreditsSequester,Exports,ObligatedCFS,OProd,PolConv,SqCredits,SqPolNet) = data
  (; SqPolTotal) = data

  #@debug "  SuPollutionCFS.jl - Control - SequesteringCredits"

  for area in areas, ecc in ECCs
    CreditsSequester[ecc,area] = 
      sum(0-SqPolNet[ecc,poll,area]*PolConv[poll] for poll in Polls)*  
      ObligatedCFS[ecc,area]*SqCredits[market]
  end

  CrudeOil = Select(FuelEP,"CrudeOil")
  CN = Select(Nation,"CN")
  eccs = Select(ECC,["LightOilMining","HeavyOilMining"])
  for area in areas, ecc in eccs
    CreditsSequester[ecc,area] = 
      sum(0-SqPolNet[ecc,poll,area]*PolConv[poll] for poll in Polls)*  
      ObligatedCFS[ecc,area]*SqCredits[market]*
      (1-Exports[CrudeOil,CN]/sum(OProd[process,CN] for process in Processes))
  end

  WriteDisk(db,"SOutput/CreditsSequester",year,CreditsSequester)

end

function FossilCredits(data::Data,areas,market)
  (; db,year) = data
  (; ECCs,Fuel,Fuels,Poll) = data
  (; CgPol,CgPolRef,CreditsFossil,CreditsFossilGenerated,CreditsFossilLimit) = data
  (; CreditsFossilMaximum,CreditsNeeded,DirectCredits,Driver,DriverRef) = data
  (; EuPol,EnPolRef,FlPol,FlPolRef,FuPol,FuPolRef,MEPol,MEPolRef) = data
  (; NcPol,NcPolRef,ObligatedCFS,PolConv,VnPol,VnPolRef) = data
  (; DirectEICFS,DirectEICFSRef,DirectEmissions,DirectEmissionsRef) = data  

  #@debug "  SuPollutionCFS.jl - Control - FossilCredits"

  GHG = Select(Poll,["CO2","CH4","N2O","SF6","PFC","HFC"])

  for area in areas, ecc in ECCs
    DirectEmissions[ecc,area] = sum((EuPol[ecc,poll,area]+
      CgPol[ecc,poll,area]+NcPol[ecc,poll,area]+MEPol[ecc,poll,area]+
      FlPol[ecc,poll,area]+FuPol[ecc,poll,area]+VnPol[ecc,poll,area])*PolConv[poll]
      for poll in GHG)
    DirectEmissionsRef[ecc,area] = sum((EnPolRef[ecc,poll,area]+
      CgPolRef[ecc,poll,area]+NcPolRef[ecc,poll,area]+MEPolRef[ecc,poll,area]+
      FlPolRef[ecc,poll,area]+FuPolRef[ecc,poll,area]+VnPolRef[ecc,poll,area])*PolConv[poll]
      for poll in GHG)

    @finite_math DirectEICFS[ecc,area] = DirectEmissions[ecc,area]/Driver[ecc,area]
    @finite_math DirectEICFSRef[ecc,area] = DirectEmissionsRef[ecc,area]/DriverRef[ecc,area]
  end

  for area in areas
    CreditsFossilMaximum[area] = sum(CreditsNeeded[fuel,ecc,area]*CreditsFossilLimit[ecc,area]
      for ecc in ECCs, fuel in Fuels)
  end

  fuel = Select(Fuel,"Gasoline")
  for area in areas, ecc in ECCs
    CreditsFossil[fuel,ecc,area] = max(DirectEICFSRef[ecc,area]-DirectEICFS[ecc,area],0)*
      Driver[ecc,area]*ObligatedCFS[ecc,area]*DirectCredits[market]
  end

  for area in areas
    CreditsFossilGenerated[area] = sum(CreditsFossil[fuel,ecc,area] for ecc in ECCs, fuel in Fuels)
  end

  for area in areas, fuel in Fuels, ecc in ECCs
    @finite_math CreditsFossil[fuel,ecc,area] = CreditsFossil[fuel,ecc,area]/
      CreditsFossilGenerated[area]*CreditsFossilMaximum[area]
  end

  WriteDisk(db,"SOutput/CreditsFossil",year,CreditsFossil)

end

function GeneratedCredits(data::Data,areas)
  (; db,year) = data
  (; ECCs,Fuels) = data #sets
  (; CreditsCogeneration,CreditsFossil,CreditsFugitive,CreditsGenerated) = data
  (; CreditsLowCarbon,CreditsOther,CreditsSequester) = data

  for area in areas
    CreditsGenerated[area] = sum(CreditsLowCarbon[fuel,ecc,area] for ecc in ECCs, fuel in Fuels)+
                           sum(CreditsFossil[fuel,ecc,area] for ecc in ECCs, fuel in Fuels)+
                           sum(CreditsFugitive[ecc,area] for ecc in ECCs)+
                           sum(CreditsCogeneration[ecc,area] for ecc in ECCs)+
                           sum(CreditsSequester[ecc,area] for ecc in ECCs)+
                               CreditsOther[area]
    end

  WriteDisk(db,"SOutput/CreditsGenerated",year,CreditsGenerated)

end

