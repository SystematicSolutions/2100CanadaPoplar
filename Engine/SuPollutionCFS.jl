function CreditsForMarket(data::Data,areas,market)
  (; db,year) = data
  (; CreditsGenerated,CreditsInventoryPrior,CreditsRequired,ETRSw) = data
  (; ETADAP,FSell,ISell,TBuy,TFP3Switch,TSell,xETAPr,xFSell,xISell) = data

  #@debug "  SuPollutionCFS.jl - Control - CreditsForMarket"

  if ETRSw[market] == 0
    ISell[market] = xISell[market]
    WriteDisk(db,"SOutput/ISell",year,ISell)
    
    if xETAPr[market] >= ETADAP[market]
      FSell[market] = xFSell[market]
      WriteDisk(db,"SOutput/FSell",year,FSell)
    end
  end

  TBuy[market] = sum(CreditsRequired[area] for area in areas)

  if TFP3Switch[market] == 2
    TSell[market] = sum(CreditsGenerated[area] for area in areas)+
      CreditsInventoryPrior[market]+xFSell[market]
  else
    TSell[market] = sum(CreditsGenerated[area] for area in areas)+
      CreditsInventoryPrior[market]
  end
  WriteDisk(db,"SOutput/TBuy",year,TBuy)
  WriteDisk(db,"SOutput/TSell",year,TSell)

end

function CreditsBanking(data::Data,areas,market)
  (; db,year) = data
  (; CreditsBanked,CreditsGenerated,CreditsInventory,CreditsInventoryPrior) = data
  (; CreditsRedeemed,CreditsRequired,FSell,ISell,OverBAB) = data  

  #@debug "  SuPollutionCFS.jl - CFSCredits - CreditsBanking"

  #
  # Overage before adjustment to Banking
  #
  OverBAB[market] = sum(CreditsRequired[area]-CreditsGenerated[area] for area in areas)-
                  FSell[market]-ISell[market]

  WriteDisk(db,"SOutput/OverBAB",year,OverBAB)

  #
  # Force Redemption of Banked Credits to meet Goal
  #
  CreditsRedeemed[market] = max(min(OverBAB[market],CreditsInventoryPrior[market]),0)

  #
  #  Force Banking of Credits when Goal is exceeded
  #
  CreditsBanked[market] = max((0-OverBAB[market]),0)

  #
  #  Update Credit Inventory
  #
  CreditsInventory[market] = CreditsInventoryPrior[market]+
                           DT*(CreditsBanked[market]-CreditsRedeemed[market])  


  WriteDisk(db,"SOutput/CreditsBanked",year,CreditsBanked)
  WriteDisk(db,"SOutput/CreditsInventory",year,CreditsInventory)
  WriteDisk(db,"SOutput/CreditsRedeemed",year,CreditsRedeemed)

end

function Control(data::Data)
  (; Markets) = data #sets
  (; CapTrade,AreaMarket) = data

  @debug "  SuPollutionCFS.jl - Control"

  # Prior=Current-1
  # Prior=xmax(1,Prior)
  # Prior2=xmax(1,Prior-1)
  # 
  for market in Markets
    if (CapTrade[market] == 6) || (CapTrade[market] == 7)
      areas = findall(AreaMarket[:,market] .== 1)
      if !isempty(areas)
        #  ReferenceValues
        EIGap(data,areas)
        CFSDemand(data,areas)
        RequiredCredits(data,areas)
        LowCarbonFuelCredits(data,areas)
        FugitiveCredits(data,areas)
        CogenerationCredits(data,areas,market)
        SequesteringCredits(data,areas,market)
        FossilCredits(data,areas,market)
        GeneratedCredits(data,areas)
        TechFundPart3Credits(data,areas,market)
        CreditsForMarket(data,areas,market)
      end
    end
  end


end # function Control

