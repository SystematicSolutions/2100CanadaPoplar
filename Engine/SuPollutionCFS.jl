function CFSPrices(data::Data)
  (; db,year) = data
  (; Areas,ESes,Fuels,Markets) = data #sets
  (; AreaMarket,CapTrade,EIGapCFSPrior,ETAPr,ExchangeRate) = data
  (; FPCFSCredit,FPCFSFuel,FPCFSObligated,ObligatedCFS,SecMap) = data

  #@debug "  SuPollutionCFS.jl - CFSPrices"

  for market in Markets
    if (CapTrade[market] == 6) || (CapTrade[market] == 7)
      for area in Areas
        if AreaMarket[area,market] == 1
          for es in ESes, fuel in Fuels
            FPCFSCredit[fuel,es,area] = ETAPr[market]
            FPCFSFuel[fuel,es,area] = EIGapCFSPrior[fuel,es,area]*
              FPCFSCredit[fuel,es,area]*ExchangeRate[area]/1000000
          end
          for es in ESes
            eccs = findall(SecMap .== es)
            for ecc in eccs
              FPCFSObligated[ecc,area] = maximum(FPCFSCredit[:,es,area])*ObligatedCFS[ecc,area]
            end
          end
        end
      end
    end
  end

  WriteDisk(db,"SOutput/FPCFSCredit",year,FPCFSCredit)
  WriteDisk(db,"SOutput/FPCFSFuel",year,FPCFSFuel)
  WriteDisk(db,"SOutput/FPCFSObligated",year,FPCFSObligated)

end # function CFSPrices

