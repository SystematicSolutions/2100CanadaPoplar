function EIGap(data::Data,areas)
  (; db,year) = data
  (; ESes,Fuels) = data #sets
  (; EIGapCFS,EIGoalCFS,EIOfficial) = data

  @debug "  SuPollutionCFS.jl - Control - EIGap"

  for area in areas, es in ESes, fuel in Fuels
    EIGapCFS[fuel,es,area] = EIOfficial[fuel,area]-EIGoalCFS[fuel,es,area]
  end

  WriteDisk(db,"SOutput/EIGapCFS",year,EIGapCFS)

end

function CFSDemand(data::Data,areas)
  (; db,year) = data
  (; ECCs,Fuels) = data #sets
  (; CgDemand,CoverageCFS,DemandCFS,EuDemand) = data

  @debug "  SuPollutionCFS.jl - Control - CFSDemand"
  
  #
  # Demands Covered by CFS
  #
  for area in areas, ecc in ECCs, fuel in Fuels
    DemandCFS[fuel,ecc,area] = (EuDemand[fuel,ecc,area]+CgDemand[fuel,ecc,area])*CoverageCFS[fuel,ecc,area]
  end

  WriteDisk(db,"SOutput/DemandCFS",year,DemandCFS)

end

function RequiredCredits(data::Data,areas)
  (; db,year) = data
  (; ECCs,ESes,Fuels) = data #sets
  (; CreditsNeeded,CreditsRequired,CreditSwitch,DemandCFS,EIGoalCFS,EIOfficial,SecMap) = data

  @debug "  SuPollutionCFS.jl - Control - RequiredCredits"

  for es in ESes
    eccs = findall(SecMap .== es)
    for fuel in Fuels, ecc in eccs, area in areas
      CreditsNeeded[fuel,ecc,area] = 
        max(EIOfficial[fuel,area]-EIGoalCFS[fuel,es,area],0)*
        DemandCFS[fuel,ecc,area]*CreditSwitch[fuel,ecc,area]
    end
  end

  WriteDisk(db,"SOutput/CreditsNeeded",year,CreditsNeeded)

  for area in areas
    CreditsRequired[area] = sum(CreditsNeeded[fuel,ecc,area] for ecc in ECCs, fuel in Fuels)
  end

  WriteDisk(db,"SOutput/CreditsRequired",year,CreditsRequired)

end

function LowCarbonFuelCredits(data::Data,areas)
  (; db,year) = data
  (; Area,ECCs,ES,ESes,Fuel,Fuels) = data #sets
  (; CreditsLowCarbon,CreditsRequired,CreditSwitch,DemandCFS,EIGoalCFS,EIOfficial,SecMap) = data
  CreditsGaseousGenerated::VariableArray{1} = zeros(Float32,length(Area))
  CreditsGaseousMaximum::VariableArray{1} = zeros(Float32,length(Area))

  #@debug "  SuPollutionCFS.jl - Control - LowCarbonFuelCredits

  for area in areas, es in ESes, fuel in Fuels
    if ES[es] != "Transport"
      eccs = findall(SecMap .== es)
      for ecc in eccs
          CreditsLowCarbon[fuel,ecc,area] = max(EIGoalCFS[fuel,es,area]-EIOfficial[fuel,area],0)*DemandCFS[fuel,ecc,area]*(1-CreditSwitch[fuel,ecc,area])
      end
    end
  end

  #
  # Limit Credits from Gaseous stream
  #
  gaseousfuels = Select(Fuel,["Hydrogen","LPG","NaturalGas","RNG"])
  for area in areas
    CreditsGaseousMaximum[area] = CreditsRequired[area]*0.10
    CreditsGaseousGenerated[area] = sum(CreditsLowCarbon[fuel,ecc,area] for ecc in ECCs, fuel in gaseousfuels)
    for ecc in ECCs, fuel in gaseousfuels
      @finite_math CreditsLowCarbon[fuel,ecc,area] = CreditsLowCarbon[fuel,ecc,area]/CreditsGaseousGenerated[area]*CreditsGaseousMaximum[area]
    end
  end

  WriteDisk(db,"SOutput/CreditsLowCarbon",year,CreditsLowCarbon)

end

